export class EventDirector {
  /**
   * Generate 1 to maxEvents events for a turn via the schema-validated
   * generateObject wrapper. Falls back to canned events when the LLM
   * call or validation fails after maxRetries.
   *
   * @param onUsage Optional callback invoked with the director's LLM
   *        response so the orchestrator can account for director spend in
   *        its cost telemetry. Director runs once per turn on the flagship
   *        model; without this hook the call was invisible to the per-run
   *        `cost` object returned by `runSimulation()`.
   */
  async generateEventBatch(
    ctx: DirectorContext,
    maxEvents: number = 3,
    provider: LlmProvider = 'openai',
    model: string = 'gpt-5.4',
    instructions?: string,
    onUsage?: (result: { usage?: { totalTokens?: number; promptTokens?: number; completionTokens?: number; costUSD?: number } }) => void,
    /**
     * Called with the raw caught error when the director's LLM call throws.
     * Lets the orchestrator classify the error (quota / auth / etc.) and
     * emit a `provider_error` SSE event before the existing fallback path
     * quietly substitutes canned events.
     */
    onProviderError?: (err: unknown) => void,
    /**
     * Called with the attempts count + fallback flag so the orchestrator
     * can feed per-schema retry telemetry into its cost tracker.
     */
    onSchemaAttempt?: (attempts: number, fellBack: boolean) => void,
    /** Explicit provider API key for this run. */
    apiKey?: string,
  ): Promise<DirectorEventBatch> {
    const prompt = buildDirectorPrompt(ctx, maxEvents);
    const systemInstructions = (instructions || DEFAULT_DIRECTOR_INSTRUCTIONS)
      .replace('{MAX_EVENTS}', String(maxEvents));

    try {
      // Prompt caching: the director instructions (~1500-2000 tokens) are
      // identical across all 6 turn calls in a run. systemCacheable goes
      // through cacheBreakpoint: true so Anthropic serves turns 2-6 from
      // its prefix cache at 0.1x cost. OpenAI auto-caches prompts >=1024
      // tokens.
      const directorResult = await generateValidatedObject({
        provider,
        model,
        schema: DirectorEventBatchSchema,
        schemaName: 'DirectorEventBatch',
        systemCacheable: systemInstructions,
        prompt,
        // Director emits a small event batch (~6 events × ~170 tokens
        // including descriptions + categories ≈ 1000 tokens typical).
        // 2500 caps runaway yap without risking truncation on the
        // event array.
        maxTokens: 2500,
        apiKey,
        onUsage,
        onProviderError,
      });
      const { object, fromFallback } = directorResult;
      onSchemaAttempt?.(directorResult.attempts, fromFallback);
      if (fromFallback) {
        console.log(`  [director] Schema fallback for ${ctx.actorName}, using canned`);
      } else {
        const batch: DirectorEventBatch = {
          events: object.events.slice(0, maxEvents) as DirectorEvent[],
          pacing: object.pacing,
          reasoning: object.reasoning,
        };
        console.log(`  [director] Generated ${batch.events.length} events (${batch.pacing}) for ${ctx.actorName}: ${batch.events.map(e => `"${e.title}"`).join(', ')}`);
        return batch;
      }
    } catch (err) {
      console.log(`  [director] Validated batch error for ${ctx.actorName}: ${err}`);
      // Already-classified error from generateValidatedObject fired
      // onProviderError; fall through to canned fallback so the turn
      // still runs, but a terminal quota/auth error has already
      // tripped the abort flag in the orchestrator.
    }

    const fallback = FALLBACK_EVENTS[ctx.turn % FALLBACK_EVENTS.length];
    console.log(`  [director] Using fallback: "${fallback.title}"`);
    return { events: [{ ...fallback }], pacing: 'normal', reasoning: 'fallback' };
  }

  /**
   * Get a milestone crisis (Turn 1 or final turn).
   * These are fixed for narrative anchoring.
   */
  getMilestoneCrisis(turn: number, maxTurns: number): DirectorCrisis | null {
    if (turn === 1) {
      const s = SCENARIOS[0];
      return {
        title: s.title,
        description: s.crisis,
        crisis: s.crisis,
        options: s.options || [
          { id: 'option_a', label: 'Arcadia Planitia', description: 'Flat basalt plains, safe, ice access', isRisky: false },
          { id: 'option_b', label: 'Valles Marineris rim', description: 'Canyon rim, mineral rich, hazardous terrain', isRisky: true },
        ],
        riskyOptionId: 'option_b',
        riskSuccessProbability: s.riskSuccessProbability,
        category: 'infrastructure',
        researchKeywords: s.researchKeywords,
        relevantDepartments: ['medical', 'engineering'],
        turnSummary: 'Colony ship in orbit. Safe plains or mineral-rich canyon rim: the first decision shapes everything.',
      };
    }

    if (turn === maxTurns) {
      const s = SCENARIOS[11]; // Legacy Assessment
      const crisisText = s?.crisis || 'Earth requests a comprehensive status report on your colony. Assess your legacy.';
      return {
        title: s?.title || 'Legacy Assessment',
        description: crisisText,
        crisis: crisisText,
        options: [
          { id: 'option_a', label: 'Honest assessment', description: 'Report factually, including failures and regrets', isRisky: false },
          { id: 'option_b', label: 'Ambitious projection', description: 'Emphasize achievements, propose bold next-century vision', isRisky: true },
        ],
        riskyOptionId: 'option_b',
        riskSuccessProbability: 0.5,
        category: 'political',
        researchKeywords: ['Mars colony long-term projections'],
        relevantDepartments: ['governance', 'psychology', 'medical', 'engineering'],
        turnSummary: 'Earth demands a full status report. The commander must decide: honest accounting of failures, or bold vision for the next century.',
      };
    }

    return null; // Not a milestone turn
  }
}
